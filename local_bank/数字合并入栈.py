"""
题目名称：数字合并入栈
题目来源：根据用户上传的图片整理

【题目描述】
向一个空栈中依次存入正整数。假设栈内元素 n1, n2, n3, ... ny (1 <= n <= 2^31 - 1)
按顺序依次为 nx...n4, n3, n2, n1（即 n1 是最后入栈的，在栈顶）。

每当元素入栈时，如果满足条件：n1 = n2 + n3 + ... + ny (y 的范围 [2, x], 1 <= x <= 1000)，
则 n1 到 ny 全部元素出栈，并重新入栈一个新元素 m = 2 * n1。

特殊规则：
1. 如果新元素入栈后依然满足上述条件，则继续触发合并（递归/循环合并）。
2. 输入的数字依次入栈，左边的数字先入栈。

【输入描述】
使用单个空格隔开的正整数组成的字符串，如 "5 6 7 8"。
输入的正整数个数为 x, 1 <= x <= 1000。

【输出描述】
最终栈中残留的元素值，元素值使用空格隔开。
注意：栈顶数字在左边（即需要将栈内元素反转输出）。

【示例1】
输入：5 10 20 50 85 1
输出：1 170
解析：
1. 5 入栈：[5]
2. 10 入栈：10 != 5，保持 [5, 10]
3. 20 入栈：20 != 10, 20 != 10+5，保持 [5, 10, 20]
4. 50 入栈：50 != 20...，保持 [5, 10, 20, 50]
5. 85 入栈：检查 85 是否等于前面元素的和。85 = 50 + 20 + 10 + 5，满足条件。
   弹出 85, 50, 20, 10, 5。入栈 2 * 85 = 170。
   当前栈：[170]
6. 1 入栈：1 != 170，保持 [170, 1]
7. 输出（栈顶在左）：1 170

【示例2】
输入：6 1 2 3
输出：12
解析：
1. 依次入栈：[6, 1, 2]
2. 3 入栈：检查发现 3 = 1 + 2。弹出 3, 2, 1，入栈 2 * 3 = 6。
3. 此时栈为 [6, 6]。再次检查栈顶，发现 6 = 6（满足 n1 = n2）。
4. 弹出 6, 6，入栈 2 * 6 = 12。
5. 最终栈：[12]，输出 12。
"""

import sys


def solve():
    # 读取输入
    line = sys.stdin.readline().strip()
    if not line:
        return

    nums = list(map(int, line.split()))

    # 使用列表模拟栈
    stack = []

    for n in nums:
        # 当前尝试入栈的数字
        current = n

        while True:
            # 检查当前栈顶元素 current 是否等于栈中若干个连续元素的和
            found = False
            temp_sum = 0
            # 从栈顶向下搜索
            for i in range(len(stack) - 1, -1, -1):
                temp_sum += stack[i]
                if temp_sum == current:
                    # 找到了满足条件的序列
                    # 弹出从 i 到末尾的所有元素
                    stack = stack[:i]
                    # 产生新的待入栈元素
                    current = 2 * current
                    found = True
                    break
                elif temp_sum > current:
                    # 如果累加和已经超过当前值，不可能再相等，提前结束
                    break

            # 如果没有触发合并，则将当前元素入栈并处理下一个输入数字
            if not found:
                stack.append(current)
                break

    # 输出要求：栈顶数字在左边，即反转输出
    stack.reverse()
    print(" ".join(map(str, stack)))


if __name__ == "__main__":
    # 执行解题函数
    solve()

"""
【逻辑分析】
1. **栈结构**：本题的核心在于“后进先出”后的追溯合并，非常适合用栈来处理。
2. **贪心合并**：每进入一个新数字，我们不是只检查一次，而是要循环检查（while True）。
   因为 2*n1 入栈后，它可能又等于之前若干数字的和，从而引发链式反应（如示例2所示）。
3. **求和逻辑**：
   - 设当前入栈数为 `current`。
   - 我们从栈顶向栈底遍历，不断累加。
   - 如果 `sum == current`，说明找到了匹配项，执行合并。
   - 如果 `sum > current`，说明已经追溯得太远了，无法匹配，停止对当前 `current` 的合并尝试。
4. **复杂度**：
   - 外部循环遍历输入（N = 1000）。
   - 内部求和遍历栈（最大深度 N）。
   - 总体复杂度约为 O(N^2)，对于 N=1000 的规模，Python 可以在毫秒级完成计算。
"""